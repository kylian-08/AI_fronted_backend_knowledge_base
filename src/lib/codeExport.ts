interface T {
  bg: string
  fg: string
  primary: string
  primaryFg: string
  muted: string
  mutedFg: string
  border: string
  radius: string
  shadow: string
  font: string
  backdrop?: string
}

function pickTokens(tokens: Tokens): T {
  const bg = tokens['--sc-bg'] ?? '#0f172a'
  return {
    bg,
    fg: tokens['--sc-fg'] ?? '#f8fafc',
    primary: tokens['--sc-primary'] ?? '#6366f1',
    primaryFg: tokens['--sc-primary-fg'] ?? '#ffffff',
    muted: tokens['--sc-muted'] ?? '#1e293b',
    mutedFg: tokens['--sc-muted-fg'] ?? '#94a3b8',
    border: tokens['--sc-border'] ?? '#334155',
    radius: tokens['--sc-radius'] ?? '8px',
    shadow: tokens['--sc-shadow'] ?? '0 4px 12px rgba(0,0,0,0.3)',
    font: tokens['--sc-font'] ?? 'Inter, system-ui, sans-serif',
    backdrop: tokens['--sc-backdrop'],
  }
}

function tokensConst(t: T): string {
  const lines = [
    `  bg: '${t.bg}',`,
    `  fg: '${t.fg}',`,
    `  primary: '${t.primary}',`,
    `  primaryFg: '${t.primaryFg}',`,
    `  muted: '${t.muted}',`,
    `  mutedFg: '${t.mutedFg}',`,
    `  border: '${t.border}',`,
    `  radius: '${t.radius}',`,
    `  shadow: '${t.shadow}',`,
    `  font: '${t.font}',`,
  ]
  if (t.backdrop) lines.push(`  backdrop: '${t.backdrop}',`)
  return `const T = {\n${lines.join('\n')}\n} as const`
}

function pascalCase(id: string): string {
  return id
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((w) => w[0].toUpperCase() + w.slice(1))
    .join('')
}

