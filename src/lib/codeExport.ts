/**
 * Phase 2 (Frontend Code Direct Output): turn the current style tokens /
 * component preview / motion parameters into standalone, paste-ready code
 * strings. Everything here is pure string generation — no runtime coupling
 * with the preview implementation.
 */

type Tokens = Record<string, string>

/** Ordered, human-friendly listing of the `--sc-*` token keys we emit. */
const TOKEN_ORDER = [
  '--sc-bg',
  '--sc-bg-fallback',
  '--sc-fg',
  '--sc-primary',
  '--sc-primary-fg',
  '--sc-muted',
  '--sc-muted-fg',
  '--sc-border',
  '--sc-radius',
  '--sc-shadow',
  '--sc-font',
  '--sc-backdrop',
]

function orderedEntries(tokens: Tokens): [string, string][] {
  const known = TOKEN_ORDER.filter((k) => tokens[k] != null).map((k) => [k, tokens[k]] as [string, string])
  const rest = Object.entries(tokens)
    .filter(([k]) => !TOKEN_ORDER.includes(k))
    .sort(([a], [b]) => a.localeCompare(b))
  return [...known, ...rest]
}

/* ------------------------------------------------------------------ */
/* Token export                                                        */
/* ------------------------------------------------------------------ */

export function tokensToCssVars(tokens: Tokens, styleName?: string): string {
  const lines = orderedEntries(tokens).map(([k, v]) => `  ${k}: ${v};`)
  return [
    `/* Design tokens${styleName ? ` — "${styleName}"` : ''} · generated by Prompt Assistant */`,
    ':root {',
    ...lines,
    '}',
    '',
    '/* Usage: background: var(--sc-bg); color: var(--sc-fg); etc. */',
  ].join('\n')
}

/** Bare `:root { ... }` block, no header/usage comments — meant for embedding elsewhere (e.g. prompts). */
function tokensToCssVarsBlock(tokens: Tokens): string {
  const lines = orderedEntries(tokens).map(([k, v]) => `  ${k}: ${v};`)
  return [':root {', ...lines, '}'].join('\n')
}

/**
 * Appends a ready-to-paste CSS variables block to a style's prompt text, so
 * that copying the prompt into any AI/dev tool carries exact color/token
 * values alongside the free-text description — not just a verbal summary
 * that's open to interpretation. Applied once to all bundled styles at
 * catalog build time (see `generateAllStyles()`); left untouched for
 * user-imported styles so we never double-append.
 */
export function appendCodeSampleToPrompt(style: StyleItem): StyleItem {
  const cssBlock = tokensToCssVarsBlock(style.tokens)
  return {
    ...style,
    prompt: {
      'zh-CN': `${style.prompt['zh-CN']}\n\n对应 CSS 变量（可直接使用，确保生成结果的配色/圆角/字体与设计完全一致）：\n\`\`\`css\n${cssBlock}\n\`\`\``,
      'en-US': `${style.prompt['en-US']}\n\nCorresponding CSS variables (use directly to keep colors/radius/font exactly on-spec):\n\`\`\`css\n${cssBlock}\n\`\`\``,
    },
  }
}

export function tokensToTailwindConfig(tokens: Tokens, styleName?: string): string {
  const fontStack = (tokens['--sc-font'] ?? 'Inter, system-ui, sans-serif')
    .split(',')
    .map((f) => `'${f.trim().replace(/^['"]|['"]$/g, '')}'`)
    .join(', ')
  return `// tailwind.config.js — theme.extend${styleName ? ` for "${styleName}"` : ''} · generated by Prompt Assistant
/** @type {import('tailwindcss').Config} */
export default {
  theme: {
    extend: {
      colors: {
        background: '${tokens['--sc-bg-fallback'] ?? tokens['--sc-bg'] ?? '#0f172a'}',
        foreground: '${tokens['--sc-fg'] ?? '#f8fafc'}',
        primary: {
          DEFAULT: '${tokens['--sc-primary'] ?? '#6366f1'}',
          foreground: '${tokens['--sc-primary-fg'] ?? '#ffffff'}',
        },
        muted: {
          DEFAULT: '${tokens['--sc-muted'] ?? '#1e293b'}',
          foreground: '${tokens['--sc-muted-fg'] ?? '#94a3b8'}',
        },
        border: '${tokens['--sc-border'] ?? '#334155'}',
      },
      borderRadius: {
        theme: '${tokens['--sc-radius'] ?? '8px'}',
      },
      boxShadow: {
        theme: '${tokens['--sc-shadow'] ?? '0 4px 12px rgba(0,0,0,0.3)'}',
      },
      fontFamily: {
        theme: [${fontStack}],
      },
    },
  },
}`
}

